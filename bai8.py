n = input("Nhập điểm: ")
if n == "A":
    print("sinh viên xuất sắc ")
elif n == "B":
    print("sinh viên loại giỏi ")
elif n == "C":
    print("sinh viên loại khá ")
elif n == "D":
    print("sinh viên loại trung bình ")
elif n == "E":
    print("sinh viên loại yếu ")
elif n == "F":
    print("sinh viên xếp loại kém ")
else:
    print("Yêu cầu nhập lại điểm của sinh viên ")
